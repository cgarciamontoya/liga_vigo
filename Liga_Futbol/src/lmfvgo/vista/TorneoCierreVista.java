        torneo = torneoDAO.torneoActivo();
        
        txtNombre.setText(torneo.getNombre());
        
        List<EstadisticasJugador> goleo1 = estadisticasEquipoDAO.consultaGoleo(1);
        List<EstadisticasJugador> goleo2 = estadisticasEquipoDAO.consultaGoleo(2);
        
