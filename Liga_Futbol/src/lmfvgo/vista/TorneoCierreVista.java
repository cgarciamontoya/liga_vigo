    private void cerrarTorneo(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cerrarTorneo
        int dialogResult = JOptionPane.showConfirmDialog(null, "Está seguro que desea cerrar el torneo?", "Advertencia", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {
                if (txtFechaFin.getText() == null || txtFechaFin.getText().trim().isEmpty()) {
                agregarMensajeAdvertencia("La fecha de fin es requerida");
                return;
            } else {
                try {
                    Date fechaFin = new SimpleDateFormat("dd/MM/yyyy").parse(txtFechaFin.getText());
                    torneo.setFechaFin(fechaFin);
                } catch (Exception ex) {
                    agregarMensajeAdvertencia("El formato de la fecha de inicio es incorrecto (dd/MM/yyyy)");
                    return;
                }
            }
            if (torneo.getFechaFin().before(torneo.getFechaInicio())) {
                agregarMensajeAdvertencia("La Fecha de Fin no puede ser menor a la Fecha de Inicio");
                return;
            }
            if (txtCampeon1.getText() == null || txtCampeon1.getText().isEmpty()) {
                agregarMensajeAdvertencia("Debe seleccionar el Campeón de Primera");
                return;
            } else {
                torneo.setCampeonPrimera(
                        Integer.parseInt(txtCampeon1.getText().split(" - ")[0]));
            }
            if (txtSub1.getText() == null || txtSub1.getText().isEmpty()) {
                agregarMensajeAdvertencia("Debe seleccionar el Subcampeón de Primera");
                return;
            } else {
                torneo.setSubcampeonPrimera(
                        Integer.parseInt(txtSub1.getText().split(" - ")[0]));
            }
            if (txtCampeon2.getText() == null || txtCampeon2.getText().isEmpty()) {
                agregarMensajeAdvertencia("Debe seleccionar el Campeón de Segunda");
                return;
            } else {
                torneo.setCampeonSegunda(
                        Integer.parseInt(txtCampeon2.getText().split(" - ")[0]));
            }
            if (txtSub2.getText() == null || txtSub2.getText().isEmpty()) {
                agregarMensajeAdvertencia("Debe seleccionar el Subcampeón de Segunda");
                return;
            } else {
                torneo.setSubcampeonPrimera(
                        Integer.parseInt(txtSub2.getText().split(" - ")[0]));
            }
            if (torneo.getCampeonPrimera().equals(torneo.getSubcampeonPrimera())) {
                agregarMensajeAdvertencia("El Campeón y Subcampeón de Primera no puede ser el mismo equipo");
                return;
            }
            if (torneo.getCampeonSegunda().equals(torneo.getSubcampeonSegunda())) {
                agregarMensajeAdvertencia("El Campeón y Subcampeón de Segunda no puede ser el mismo equipo");
                return;
            }
            try {
                torneoDAO.cerrarTorneo(torneo);
            } catch (LMFVGOException ex) {
                agregarMensajeError(ex.getMessage());
            }
        }
    }//GEN-LAST:event_cerrarTorneo


