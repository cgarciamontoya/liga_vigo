    private void cerrarTorneo(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cerrarTorneo
        int dialogResult = JOptionPane.showConfirmDialog(null, "Est谩 seguro que desea cerrar el torneo?", "Advertencia", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {
                if (txtFechaFin.getText() == null || txtFechaFin.getText().trim().isEmpty()) {
                agregarMensajeAdvertencia("La fecha de fin es requerida");
                return;
            } else {
                try {
                    Date fechaFin = new SimpleDateFormat("dd/MM/yyyy").parse(txtFechaFin.getText());
                    torneo.setFechaFin(fechaFin);
                } catch (Exception ex) {
                    agregarMensajeAdvertencia("El formato de la fecha de inicio es incorrecto (dd/MM/yyyy)");
                    return;
                }
            }
            if (torneo.getFechaFin().before(torneo.getFechaInicio())) {
                agregarMensajeAdvertencia("La Fecha de Fin no puede ser menor a la Fecha de Inicio");
                return;
            }
            if (cboCampeonPrimera.getSelectedIndex() == 0) {
                agregarMensajeAdvertencia("Debe seleccionar el Campe贸n de Primera");
                return;
            } else {
                torneo.setCampeonPrimera(
                        Integer.parseInt(cboCampeonPrimera.getSelectedItem().toString().split(" - ")[0]));
            }
            if (cboSubPrimera.getSelectedIndex() == 0) {
                agregarMensajeAdvertencia("Debe seleccionar el Subcampe贸n de Primera");
                return;
            } else {
                torneo.setSubcampeonPrimera(
                        Integer.parseInt(cboSubPrimera.getSelectedItem().toString().split(" - ")[0]));
            }
            if (cboCampeonSegunda.getSelectedIndex() == 0) {
                agregarMensajeAdvertencia("Debe seleccionar el Campe贸n de Segunda");
                return;
            } else {
                torneo.setCampeonSegunda(
                        Integer.parseInt(cboCampeonSegunda.getSelectedItem().toString().split(" - ")[0]));
            }
            if (cboSubSegunda.getSelectedIndex() == 0) {
                agregarMensajeAdvertencia("Debe seleccionar el Subcampe贸n de Segunda");
                return;
            } else {
                torneo.setSubcampeonPrimera(
                        Integer.parseInt(cboSubSegunda.getSelectedItem().toString().split(" - ")[0]));
            }
            if (torneo.getCampeonPrimera().equals(torneo.getSubcampeonPrimera())) {
                agregarMensajeAdvertencia("El Campe贸n y Subcampe贸n de Primera no puede ser el mismo equipo");
                return;
            }
            if (torneo.getCampeonSegunda().equals(torneo.getSubcampeonSegunda())) {
                agregarMensajeAdvertencia("El Campe贸n y Subcampe贸n de Segunda no puede ser el mismo equipo");
                return;
            }
            try {
                torneoDAO.cerrarTorneo(torneo);
            } catch (LMFVGOException ex) {
                agregarMensajeError(ex.getMessage());
            }
        }
    }//GEN-LAST:event_cerrarTorneo


