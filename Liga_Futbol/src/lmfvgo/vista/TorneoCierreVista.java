    private void llenarCombo(JComboBox combo, List<Equipos> equipos) {
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) combo.getModel();
        modelo.addElement("0 - Seleccione");
        for (Equipos eq : equipos) {
            modelo.addElement(eq.getIdEquipo() + " - " + eq.getNombre());
        }
    }

